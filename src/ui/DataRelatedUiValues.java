package ui;

public class DataRelatedUiValues {
	public int dx;
	public int dy;
	public int ratioX;
	public int ratioY;

	public DataRelatedUiValues() {
	}

	public DataRelatedUiValues(int dx, int dy, int ratioX, int ratioY) {
		super();
		this.dx = dx;
		this.dy = dy;
		this.ratioX = ratioX;
		this.ratioY = ratioY;
	}
}